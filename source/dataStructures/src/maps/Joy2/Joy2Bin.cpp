#include "Joy2Bin.h"

//////////////////////////////////////////////////////////////////
/// Joy2Bin::Joy2Bin()
//////////////////////////////////////////////////////////////////
Joy2Bin::Joy2Bin() : m_isFree(true), m_key(0) {}

//////////////////////////////////////////////////////////////////
/// Joy2Bin::~Joy2Bin()
//////////////////////////////////////////////////////////////////
Joy2Bin::~Joy2Bin() {}

//////////////////////////////////////////////////////////////////
/// Joy2Bin::freeKey()
//////////////////////////////////////////////////////////////////
void Joy2Bin::free() { m_isFree = true; }

//////////////////////////////////////////////////////////////////
/// Joy2Bin::isFree()
//////////////////////////////////////////////////////////////////
bool Joy2Bin::isFree() { return m_isFree; }

//////////////////////////////////////////////////////////////////
/// Joy2Bin::setKey()
//////////////////////////////////////////////////////////////////
void Joy2Bin::setKey(const uint32_t key)
{
    m_isFree = false;
    m_key = key;
}

//////////////////////////////////////////////////////////////////
/// Joy2Bin::getKey()
//////////////////////////////////////////////////////////////////
uint32_t Joy2Bin::getKey() { return m_key; }